package demo;

public class JavaEscapeSequence {
    public static void main(String[] args) {
        System.out.println("The name of this place is \' PNT \' ");
        System.out.println("The name of this place is \" PNT \" ");
        System.out.println("The name of this place is \\ PNT \\ ");
        System.out.println("The name of this place is \tPNT  ");
        System.out.println("The name of this place is \nPNT  ");
        System.out.println("The name of this place is \bPNT  ");
    }
}
